package com.company.firstpackage;

public class DifferentTypeOfVariable {

    public static void main(String[] args) {
            int a = 28;
            boolean b = true;
            long c = 123432367897766778l;
            short d= 213;
            char e = 'A';
            byte f = 2 ;
            double g = 12345678d;
            float h = 2345.2345f;
            String i = "jahbdgjd";

        System.out.println("This is int :" + a); // Here previous a will print
        System.out.println("This is boolean :" + b); // Here previous b will print
        System.out.println("This is long :" + c);
        System.out.println("This is short :" + d);
        System.out.println("This is char :" + e);
        System.out.println("This is byte :" + f);
        System.out.println("This is double :" + g);
        System.out.println("This is float :" + h);
        System.out.println("This is String :" + i);

        a=25 ;
        b= false;

        System.out.println("This is int :" + a); //Here the value of a is changed. Changed value will print a = 25
        System.out.println("This is boolean :" + b); //Here the value of b is changed. Changed value will print b = false






    }
}
